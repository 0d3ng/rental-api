package com.frent.helper;

public class TransaksiBatalHelper {
	
	private String id;
	private String transaksiId;
	private String tanggal;
	private String nama;
	private String telp;
	private String alasan;
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the transaksiId
	 */
	public String getTransaksiId() {
		return transaksiId;
	}
	/**
	 * @param transaksiId the transaksiId to set
	 */
	public void setTransaksiId(String transaksiId) {
		this.transaksiId = transaksiId;
	}
	/**
	 * @return the tanggal
	 */
	public String getTanggal() {
		return tanggal;
	}
	/**
	 * @param tanggal the tanggal to set
	 */
	public void setTanggal(String tanggal) {
		this.tanggal = tanggal;
	}
	/**
	 * @return the nama
	 */
	public String getNama() {
		return nama;
	}
	/**
	 * @param nama the nama to set
	 */
	public void setNama(String nama) {
		this.nama = nama;
	}
	/**
	 * @return the telp
	 */
	public String getTelp() {
		return telp;
	}
	/**
	 * @param telp the telp to set
	 */
	public void setTelp(String telp) {
		this.telp = telp;
	}
	/**
	 * @return the alasan
	 */
	public String getAlasan() {
		return alasan;
	}
	/**
	 * @param alasan the alasan to set
	 */
	public void setAlasan(String alasan) {
		this.alasan = alasan;
	}
	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "TransaksiBatalHelper [id=" + id + ", transaksiId=" + transaksiId + ", tanggal=" + tanggal + ", nama="
				+ nama + ", telp=" + telp + ", alasan=" + alasan + "]";
	}
	
	
	

}
