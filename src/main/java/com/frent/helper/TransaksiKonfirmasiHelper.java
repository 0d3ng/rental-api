package com.frent.helper;

public class TransaksiKonfirmasiHelper {
	
	private String transaksiId;
	private String buktiTransfer;
	private String note;
	private int userId;
	/**
	 * @return the transaksiId
	 */
	public String getTransaksiId() {
		return transaksiId;
	}
	/**
	 * @param transaksiId the transaksiId to set
	 */
	public void setTransaksiId(String transaksiId) {
		this.transaksiId = transaksiId;
	}
	/**
	 * @return the buktiTransfer
	 */
	public String getBuktiTransfer() {
		return buktiTransfer;
	}
	/**
	 * @param buktiTransfer the buktiTransfer to set
	 */
	public void setBuktiTransfer(String buktiTransfer) {
		this.buktiTransfer = buktiTransfer;
	}
	/**
	 * @return the note
	 */
	public String getNote() {
		return note;
	}
	/**
	 * @param note the note to set
	 */
	public void setNote(String note) {
		this.note = note;
	}
	/**
	 * @return the userId
	 */
	public int getUserId() {
		return userId;
	}
	/**
	 * @param userId the userId to set
	 */
	public void setUserId(int userId) {
		this.userId = userId;
	}
	
	

}
